export function add(x, y) {
  return x + y;
}

export function reduce(x, y) {
  return x - y;
}

export function multiplication(x, y) {
  return x * y;
}

export function deliver(x, y) {
  return x / y;
}

export function power(x, y) {
  return x ** y;
}

export function addOne(y) {
  return 1 + y;
}
